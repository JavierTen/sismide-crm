    <ul>
        <li><strong>Escala:</strong> 1 a 10 puntos por cada criterio</li>
        <li><strong>Total de criterios:</strong> 11 preguntas</li>
        <li><strong>Ponderación:</strong> Cada criterio tiene una ponderación específica</li>
    </ul>

    <h4>Proceso:</h4>
    <ol>
        <li>Revise el plan de negocio del emprendedor</li>
        <li>Califique cada criterio objetivamente</li>
        <li>Agregue recomendaciones constructivas</li>
        <li>Guarde la evaluación</li>
    </ol>

